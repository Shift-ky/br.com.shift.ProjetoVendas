    private void btnEntrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrar1ActionPerformed
        // TODO add your handling code here:
         try {
            int qtd_atualizada;
            int qtd_estoque;
            
            qtd_nova = Integer.parseInt(txtQtd.getText());
            qtd_estoque = Integer.parseInt(txtAtual.getText());
            
            qtd_atualizada = qtd_estoque + qtd_nova;
            ProdutosDao dao = new ProdutosDao();
            dao.adicionarEstoque(idProduto, qtd_atualizada);
            
            txtDescricao.setText("");
            txtAtual.setText("");
            txtQtd.setText("");
            listar();
             JOptionPane.showMessageDialog(null,"Estoque de Produto atualizado");
        } catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Informe um valor nummérico");
        }
    }//GEN-LAST:event_btnEntrar1ActionPerformed

